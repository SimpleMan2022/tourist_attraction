wisata_berdasarkan_kategori(Kategori, DaftarWisata) :-
    findall(Wisata-Deskripsi, (kategori(Wisata, Kategori), objek_wisata(Wisata, Deskripsi)), DaftarWisata).

wisata_berdasarkan_kategori_list(ListKategori, DaftarWisata) :-
    findall([Wisata, Deskripsi, Biaya], (
        member(Kategori, ListKategori),
        kategori(Wisata, Kategori),
        objek_wisata(Wisata, Deskripsi),
        biaya(Wisata, Biaya)
    ), DaftarWisataDuplikat),
    list_to_set(DaftarWisataDuplikat, DaftarWisata).

wisata_berdasarkan_kategori_budget(Kategori, BudgetMin, BudgetMax, DaftarWisata) :-
    findall([Wisata, Deskripsi, Biaya], (
        kategori(Wisata, Kategori),
        objek_wisata(Wisata, Deskripsi),
        biaya(Wisata, Biaya),
        Biaya >= BudgetMin,
        Biaya =< BudgetMax
    ), DaftarWisataDuplikat),
    list_to_set(DaftarWisataDuplikat, DaftarWisata).

print_all_wisata(Kategori) :-
    wisata_berdasarkan_kategori(Kategori, WisataDeskripsiList),
    print_wisata_list(WisataDeskripsiList).

print_wisata_list([]).
print_wisata_list([Wisata-Deskripsi | T]) :-
    biaya(Wisata, Biaya),
    write(Wisata), write(': '), write(Deskripsi), write(' Biaya: '), write(Biaya), nl,
    print_wisata_list(T).

print_wisata_budget(Kategori, BudgetMin, BudgetMax) :-
    wisata_berdasarkan_kategori_budget(Kategori, BudgetMin, BudgetMax, WisataBudgetList),
    print_wisata_budget_list(WisataBudgetList).

    print_wisata_budget_list([]).
print_wisata_budget_list([[Wisata, Deskripsi, Biaya] | T]) :-
    write(Wisata), write(': '), write(Deskripsi), write(' Biaya: '), write(Biaya), nl,
    print_wisata_budget_list(T).

list_to_set([], Set, Set).
list_to_set([H|T], Temp, Set) :-
    member(H, Temp), !,
    list_to_set(T, Temp, Set).
list_to_set([H|T], Temp, Set) :-
    list_to_set(T, [H|Temp], Set).

member(X, [X|_]).
member(X, [_|T]) :- member(X, T).